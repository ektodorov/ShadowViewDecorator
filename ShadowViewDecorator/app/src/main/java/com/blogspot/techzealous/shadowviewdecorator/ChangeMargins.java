package com.blogspot.techzealous.shadowviewdecorator;

public class ChangeMargins {

	public boolean changeLeft;
	public boolean changeTop;
	public boolean changeRight;
	public boolean changeBottom;
	
	public ChangeMargins(boolean aChangeLeft, boolean aChangeTop, boolean aChangeRight, boolean aChangeBottom)
	{
		super();
		changeLeft = aChangeLeft;
		changeTop = aChangeTop;
		changeRight = aChangeRight;
		changeBottom = aChangeBottom;
	}
}
